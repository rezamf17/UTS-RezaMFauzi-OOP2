    private void tabelDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelDataMouseClicked
        // TODO add your handling code here:
        int SelectedRow = tabelData.getSelectedRow();
        DefaultTableModel dataModel = (DefaultTableModel) tabelData.getModel();
        txtId.setText(dataModel.getValueAt(SelectedRow, 0).toString());
        txtNama.setText(dataModel.getValueAt(SelectedRow, 1).toString());
        cbSatuan.setSelectedItem(dataModel.getValueAt(SelectedRow, 2).toString());
        txtJumlah.setText(dataModel.getValueAt(SelectedRow, 3).toString());
        txtHarga.setText(dataModel.getValueAt(SelectedRow, 4).toString());
        date.setValue(dataModel.getValueAt(SelectedRow, 5).toString());
    }//GEN-LAST:event_tabelDataMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int i = tabelData.getSelectedRow();
        DefaultTableModel dataModel = (DefaultTableModel) tabelData.getModel();
        dataModel.setValueAt(txtId.getText(), i, 0);
        dataModel.setValueAt(txtNama.getText(), i, 1);
        dataModel.setValueAt(cbSatuan.getSelectedItem(), i, 2);
        dataModel.setValueAt(txtJumlah.getText(), i, 3);
        dataModel.setValueAt(txtHarga.getText(), i, 4);
        dataModel.setValueAt(date.getValue(), i, 5);
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dataModel = (DefaultTableModel) tabelData.getModel();
        String search = txtSearch.getText().toLowerCase();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(dataModel);
        tabelData.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(search));
    }//GEN-LAST:event_jButton5ActionPerformed

