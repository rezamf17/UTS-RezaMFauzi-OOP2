    public void TotalHarga(){
        jumlah = Integer.valueOf(txtJumlah.getText());
        harga = Integer.valueOf(txtHarga.getText());

        totall = jumlah * harga;
        totalString = String.valueOf(totall);
        Tharga.setText(totalString);
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dataModel = (DefaultTableModel) tabelData.getModel();
            List list = new ArrayList<>();
            tabelData.setAutoCreateColumnsFromModel(true);
            list.add(txtId.getText());
            list.add(txtNama.getText());
            list.add(cbSatuan.getSelectedItem().toString());
            list.add(txtJumlah.getText());
            list.add(txtHarga.getText());
            list.add(date.getValue());
            list.add(Tharga.getText());
            dataModel.addRow(list.toArray());
    }//GEN-LAST:event_jButton1ActionPerformed

